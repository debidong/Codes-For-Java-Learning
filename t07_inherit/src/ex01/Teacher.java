package ex01;

public class Teacher {
    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    private int name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    private int age;
    private double salary;
}
